CREATE INDEX IF NOT EXISTS idx_modes_guild_id
    ON modes(guild_id);

CREATE INDEX IF NOT EXISTS idx_mode_reply_choices_mode_id
    ON mode_reply_choices(mode_id);

CREATE INDEX IF NOT EXISTS idx_mode_trigger_conditions_mode_id
    ON mode_trigger_conditions(mode_id);

CREATE INDEX IF NOT EXISTS idx_mode_exit_conditions_mode_id
    ON mode_exit_conditions(mode_id);
