CREATE TABLE IF NOT EXISTS counter_states (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    counter_id BIGINT NOT NULL REFERENCES counters(id) ON DELETE CASCADE,
    current_value INTEGER NOT NULL DEFAULT 0,
    period_key TEXT,
    last_reset_at TIMESTAMPTZ,
    state_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (guild_id, counter_id)
);

ALTER TABLE counter_states
    ADD COLUMN IF NOT EXISTS current_value INTEGER NOT NULL DEFAULT 0,
    ADD COLUMN IF NOT EXISTS period_key TEXT,
    ADD COLUMN IF NOT EXISTS last_reset_at TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS state_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    ADD COLUMN IF NOT EXISTS created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    ADD COLUMN IF NOT EXISTS updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW();

CREATE TABLE IF NOT EXISTS mode_states (
    guild_id TEXT PRIMARY KEY REFERENCES guilds(guild_id) ON DELETE CASCADE,
    current_mode_id BIGINT REFERENCES modes(id) ON DELETE SET NULL,
    active_until TIMESTAMPTZ,
    pseudo_offline_until TIMESTAMPTZ,
    shikocchi_count INTEGER NOT NULL DEFAULT 0 CHECK (shikocchi_count >= 0),
    period_states_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    state_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

ALTER TABLE mode_states
    ADD COLUMN IF NOT EXISTS current_mode_id BIGINT REFERENCES modes(id) ON DELETE SET NULL,
    ADD COLUMN IF NOT EXISTS active_until TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS pseudo_offline_until TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS shikocchi_count INTEGER NOT NULL DEFAULT 0,
    ADD COLUMN IF NOT EXISTS period_states_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    ADD COLUMN IF NOT EXISTS state_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    ADD COLUMN IF NOT EXISTS created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
