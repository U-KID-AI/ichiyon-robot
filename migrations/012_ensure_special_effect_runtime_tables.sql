CREATE TABLE IF NOT EXISTS special_effect_assignments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    special_effect_tag_id BIGINT NOT NULL REFERENCES special_effect_tags(id) ON DELETE CASCADE,
    target_type TEXT NOT NULL,
    target_id BIGINT NOT NULL,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (special_effect_tag_id, target_type, target_id)
);

CREATE INDEX IF NOT EXISTS idx_special_effect_tags_guild_id
    ON special_effect_tags(guild_id);

CREATE INDEX IF NOT EXISTS idx_special_effect_assignments_target
    ON special_effect_assignments(guild_id, target_type, target_id);
