CREATE TABLE IF NOT EXISTS counters (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    count_key TEXT NOT NULL,
    name TEXT NOT NULL,
    description TEXT,
    initial_value INTEGER NOT NULL DEFAULT 0,
    reset_type TEXT NOT NULL DEFAULT 'none',
    reset_day INTEGER,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (guild_id, count_key)
);

CREATE TABLE IF NOT EXISTS counter_states (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    counter_id BIGINT NOT NULL REFERENCES counters(id) ON DELETE CASCADE,
    current_value INTEGER NOT NULL DEFAULT 0,
    last_reset_at TIMESTAMPTZ,
    state_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (guild_id, counter_id)
);

CREATE TABLE IF NOT EXISTS modes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    mode_key TEXT NOT NULL,
    name TEXT NOT NULL,
    description TEXT,
    behavior_type TEXT NOT NULL DEFAULT 'reply',
    duration_seconds INTEGER,
    mode_icon_path TEXT,
    enter_message TEXT,
    exit_message TEXT,
    enter_gif_path TEXT,
    exit_gif_path TEXT,
    enter_notify_channel_id TEXT,
    exit_notify_channel_id TEXT,
    reaction_channel_ids JSONB NOT NULL DEFAULT '[]'::JSONB,
    ignore_channel_ids JSONB NOT NULL DEFAULT '[]'::JSONB,
    cooldown_config_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    appearance_config_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    enabled BOOLEAN NOT NULL DEFAULT FALSE,
    admin_only BOOLEAN NOT NULL DEFAULT FALSE,
    is_deletable BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (guild_id, mode_key)
);

