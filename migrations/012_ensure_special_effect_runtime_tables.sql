CREATE TABLE IF NOT EXISTS special_effect_tags (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    description TEXT,
    color TEXT NOT NULL DEFAULT '#6B7280',
    admin_only BOOLEAN NOT NULL DEFAULT FALSE,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    is_deletable BOOLEAN NOT NULL DEFAULT TRUE,
    priority INTEGER NOT NULL DEFAULT 0,
    target_type TEXT NOT NULL,
    trigger_timing TEXT NOT NULL DEFAULT 'choice_selected',
    effect_type TEXT NOT NULL,
    effect_config_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    additional_text TEXT,
    additional_post_timing TEXT NOT NULL DEFAULT 'none',
    expires_type TEXT NOT NULL DEFAULT 'permanent',
    expires_value INTEGER,
    expires_at TIMESTAMPTZ,
    cooldown_seconds INTEGER,
    cooldown_scope TEXT NOT NULL DEFAULT 'none',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

