CREATE UNIQUE INDEX IF NOT EXISTS idx_modes_guild_mode_key_unique
    ON modes(guild_id, mode_key);

CREATE TABLE IF NOT EXISTS mode_reply_choices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    mode_id BIGINT NOT NULL REFERENCES modes(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    body TEXT,
    image_path TEXT,
    appearance_rate INTEGER NOT NULL DEFAULT 1 CHECK (appearance_rate > 0),
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CHECK (body IS NOT NULL OR image_path IS NOT NULL)
);

CREATE TABLE IF NOT EXISTS mode_trigger_conditions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    mode_id BIGINT NOT NULL REFERENCES modes(id) ON DELETE CASCADE,
    condition_group_key TEXT NOT NULL DEFAULT 'default',
    group_operator TEXT NOT NULL DEFAULT 'AND' CHECK (group_operator IN ('AND', 'OR')),
    condition_type TEXT NOT NULL CHECK (
        condition_type IN (
            'probability',
            'counter_threshold',
            'period_not_triggered',
            'manual',
            'test',
            'schedule'
        )
    ),
    condition_config_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS mode_exit_conditions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guild_id TEXT NOT NULL REFERENCES guilds(guild_id) ON DELETE CASCADE,
    mode_id BIGINT NOT NULL REFERENCES modes(id) ON DELETE CASCADE,
    condition_type TEXT NOT NULL CHECK (condition_type IN ('duration_elapsed', 'manual')),
    condition_config_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS mode_states (
    guild_id TEXT PRIMARY KEY REFERENCES guilds(guild_id) ON DELETE CASCADE,
    current_mode_id BIGINT REFERENCES modes(id) ON DELETE SET NULL,
    active_until TIMESTAMPTZ,
    pseudo_offline_until TIMESTAMPTZ,
    shikocchi_count INTEGER NOT NULL DEFAULT 0 CHECK (shikocchi_count >= 0),
    period_states_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    state_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

